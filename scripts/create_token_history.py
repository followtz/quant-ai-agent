# 统计分析
type_stats = defaultdict(lambda: {'count': 0, 'total': 0})
for ts, name, token, ttype in all_data:
    type_stats[ttype]['count'] += 1
    type_stats[ttype]['total'] += token

print('\n按任务类型统计：')
for ttype, stats in sorted(type_stats.items(), key=lambda x: -x[1]['total']):
    avg = stats['total'] // stats['count']
    print(f'  {ttype}: {stats["count"]}次, 总消耗{stats["total"]:,}, 平均{avg:,}')

# 按日期统计
